    private void INSERTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INSERTActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "root12345");
            String sql = "INSERT INTO manageaircraft values(?, ?, ?, ?)";

            PreparedStatement ptst = con.prepareStatement(sql);    
            ptst.setString(1, FLIGHT_NUM.getText());
            ptst.setString(2, AIRCRAFT_ID.getText());
            ptst.setString(3, MODEL.getText());
            ptst.setString(4, MANUFACTURER.getText());
            
            ptst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data inserted successfully");
            con.close();
        } catch (Exception e) {
        }

    }//GEN-LAST:event_INSERTActionPerformed

